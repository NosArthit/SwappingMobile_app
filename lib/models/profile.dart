class Profile {
  
  String? firstName;
  String? lastName;
  String? nationalityCardId;
  String? address;
  String? tel;
  String? email;
  String? password;
  String? confirmPassword;

  Profile({
    required this.firstName,
    required this.lastName,
    required this.nationalityCardId,
    required this.address,
    required this.tel,
    required this.email,
    required this.password,
    required this.confirmPassword
  });
}

